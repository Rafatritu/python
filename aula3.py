"""
Python - linguagem de programação
tipo de tipagem = dinâmica / forte
str -> string -> texto
strings são textos que estão dentro de aspas
"""
#aspas simples
print('Rafael Francisco')

#aspas duplas
print("Rafael Francisco")
#escape
print('Rafael \"Francisco\"')
#r
print(r'Rafael \"Francisco\"')
#aspas simples dentro de aspas duplas, pode e vice-versa
print('Rafael "Francisco"')
print("Rafael 'Francisco'")